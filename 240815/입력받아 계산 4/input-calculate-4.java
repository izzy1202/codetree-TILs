import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        // 입력 받기위해 Scanner 선언
        Scanner sc = new Scanner(System.in);

        // 정수형 변수 선언
        int a;

        // 정수를 입력받아 a에 저장
        a = sc.nextInt();

        // 입력값에 2 곱한 값 출력
        System.out.print(a*2);


    }
}